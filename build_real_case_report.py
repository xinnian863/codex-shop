def rich_para(doc: Document, topic: str, focus: str, stage: str):
    add_para(
        doc,
        f"{topic}的组织应从{stage}的实际约束出发，先明确作业条件、资源条件和验收条件，再确定施工方法。"
        f"本工程为高校公共教学建筑，场地位于既有校园环境内，施工组织不能只追求单项工序速度，还要兼顾教学秩序、"
        f"通行安全、噪声控制、材料进出校门管理和周边建筑保护。{focus}应纳入班组交底、样板验收和过程检查，"
        f"每一道工序完成后均应形成自检、互检、专检记录，确保后续工序具备连续展开条件。"
    )
    add_para(
        doc,
        f"在{topic}实施过程中，项目部应将施工段划分、机械覆盖范围、材料堆放位置和临时道路组织作为一个整体考虑。"
        f"若某一工序提前或滞后，都可能影响垂直运输、脚手架搭拆、安装预留预埋和装饰穿插。施工员每日根据周计划核对"
        f"劳动力、材料、机械和工作面条件，技术负责人对关键节点进行复核，安全员对临边洞口、临电、动火和高处作业进行巡查。"
    )
    add_para(
        doc,
        f"{focus}的质量控制不宜停留在最终验收，而应前移到材料进场、样板确认、工序转换和隐蔽验收。"
        f"例如构件尺寸偏差、基层含水率、砂浆饱满度、钢筋保护层、模板拼缝、防水附加层和成品保护等问题，"
        f"一旦在后续阶段暴露，返工成本远高于过程控制。因此项目部应采用样板引路、实测实量、质量问题台账和整改闭合制度。"
    )


